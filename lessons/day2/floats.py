# floats are also known as 
# FLOATING POINT NUMBERS
# they are numbers with a decimal

# NOT all numbers can be written as a fraction of two integers!

# floating point is LOSSY, so that means it has to round things
# floating point will make imprecision errors as you get smaller

print(1 / 3)

# you can write them using decimals!
print(0.33333)

print(3.0) # Anything with a decimal is a floating point number, EVEN if it
# technically only has zero after

# you can deal with them like ints most of the time, but they are not ints
# they will generally turn ints they touch into floats

print(1.5 * 2) # creates a float
print(1.4 * 674) # definitely creates float